import Twitter from 'twitter'
import {ITweet, ITweetMetaData} from '../types/ITweet';

export class TwitterService {

    private readonly client: Twitter;

    constructor(provider: Twitter) {
        this.client = provider;
    }

    search = (searchString: string): Promise<{statuses: ITweet[], search_metadata: ITweetMetaData}> => {
        //     // return new Promise(((resolve, reject) => {
        //     //     resolve(tmp);
        //     // }));
            return new Promise(((resolve, reject) => {
                this.client.get('search/tweets', {q: searchString}, (error, results) => {
                    if (error) {
                        reject(error);
                    } else if (results) {
                        resolve({statuses: results.statuses, search_metadata: results.search_metadata});
                    }
                });
            }));
    };
}

